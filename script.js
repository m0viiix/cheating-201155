const endings = ["ии","ие","а", "я", "о", "ы", "и", "ь", "е", "у", "ю", "э", "ё", "го", "но", "ть", "ка", "на", "ня", "ец", "й", "чик", "ый", "ник", "ев", "ин", "ов", "ль", "он", "ак", "ир", "ский", "ок", "ул", "ык", "ков", "ол", "ченко", "шко", "оно"];

function removeEndings(sentString) {
    for (let i = 0; i < endings.length; i++) {
      sentString = sentString.replace(new RegExp(endings[i], 'g'), '');
    }
    return sentString;
}

function compareStrings(sent1, sent2) {
    let sent2Words = sent2.split(' ');
    let count = 0;
    for (let i = 0; i < sent2Words.length; i++) {
      if (sent1.includes(sent2Words[i])) {
        count++;
      }
    }
    let similarity = (count / sent2Words.length) * 100;
    return similarity;
}

function findMostSimilarQuestion(sent) {
    let maxSimilarity = 0;
    let mostSimilarQuestion = null;
    for (let question in Questions) {
      let similarity = compareStrings(sent, question);
      if (similarity > 50) {
        // console.log(sent)
        // console.log(question)
        // console.log(similarity)
      }
      if (similarity > maxSimilarity) {
        maxSimilarity = similarity;
        mostSimilarQuestion = question;
      }
    }
    //console.log(maxSimilarity > 80 ? mostSimilarQuestion : null)
    if (maxSimilarity >= 70) {
        return mostSimilarQuestion
    } else {
        return null
    }
  }

